import { options } from "@/app/_clients/nextAuth";
import { AuthProvider } from "@/app/_providers/AuthProviders";
import AuthNavBar from "@/components/molecules/AuthNavBar";
import type { Metadata } from "next";
import { getServerSession } from "next-auth";
import { Inter } from "next/font/google";
import type { PropsWithChildren, ReactNode } from "react";

const inter = Inter({ subsets: ["latin"] });

export const metadata: Metadata = {
  title: "Dashboard",
  description: "Generated by create next app",
};

const items = [
  { title: "Sample", href: "/sample" },
  { title: "Taonomy", href: "/taxonomy" },
  { title: "Contact", href: "/contact" },
];

type Props = PropsWithChildren<{
  dialog: ReactNode;
}>;

export default async function SampleLayout({ dialog, children }: Props) {
  const session = await getServerSession(options);
  return (
    <html lang="en">
      <body
        className={[
          inter.className,
          "h-screen flex flex-col",
          // for dialog
          "has-[dialog[open]]:overflow-hidden",
        ].join(" ")}
      >
        <AuthProvider>
          <AuthNavBar logo={"App"} items={items} session={session} />
          <main className="py-4 px-4 flex-1 overflow-y-auto">{children}</main>
        </AuthProvider>
        {dialog}
      </body>
    </html>
  );
}
